title_str = "This Is A Title"
"""
Casing Methods:
- upper(): Converts all characters in the string to uppercase.
- lower(): Converts all characters in the string to lowercase.
- capitalize(): Capitalizes the first character of the string.
- title(): Converts the string to title case.
- swapcase(): Swaps the case of all characters in the string.
- casefold(): Converts the string to casefolded (lowercase) form.
"""

### Casing methods
print(title_str.upper())  # THIS IS A TITLE
print(title_str.lower())  # this is a title
print(title_str.capitalize())  # This is a title
print(title_str.title())  # This Is A Title
print(title_str.swapcase())  # tHIS iS a tITLE
print(title_str.casefold())  # this is a title
